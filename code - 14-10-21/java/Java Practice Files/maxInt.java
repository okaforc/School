public class maxInt {
    public static void main(String[] args) {
        int val = Integer.MAX_VALUE;
        System.out.println("val: " + val);
        System.out.println("val + 5: " + (val + 5));

        char num = 9000;
        System.out.println((int) num);

        double pp = 10000.903;
        int intVal = (int) pp;
        System.out.println(intVal);
    }
}
